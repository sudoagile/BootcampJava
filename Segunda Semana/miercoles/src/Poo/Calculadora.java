public class Calculadora {

    int a;
    int b;

    // Método para sumar
    int sumar(int x, int y) {
        int suma = x + y;
        return suma;
    }

    // Método para restar
    int restar(int x, int y) {
        int resta = x - y;
        return resta;
    }

    // Método para multiplicar
    int multiplicar(int x, int y) {
        int producto = x * y;
        return producto;
    }


}
